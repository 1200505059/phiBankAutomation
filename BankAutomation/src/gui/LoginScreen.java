   private void Login(String customerIdentity,String customerPassword){
        this.getUserLoginObject().setCustomerIdentity(customerIdentity);
          this.getUserLoginObject().setCustomerPassword(customerPassword);
          if (getUserLoginObject().loginInformationIsValid()) {
              ActionSettings.setVisible(this, new AccountScreen());            
        }else{
              Dialogs.showPrivateMessage(this, "Your information is not correct\n please check your information.");
          }
    }
