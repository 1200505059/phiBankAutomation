    @Override
    public void getedits() {
       this.setLocationRelativeTo(null);
        LoginScreenPanel.setFocusable(true);
        txtID.setText(IdentityNoOriginal);
        txtPassword.setText(PasswordOriginal);
        TextSettings.setOnlyNumber(txtID);
    }

    @Override
    public boolean informationIsValid() {
        return !(this.txtID.getText().equals(this.IdentityNoOriginal)
                ||String.valueOf(this.txtPassword.getPassword()).equals(this.PasswordOriginal));              
    }

    @Override
    public AccountInformation accountInformation() {
    return AccountInformation.getInstace();
    }

    public userLogin getUserLoginObject() {
       if(this.userLoginObject==null){
           userLoginObject=new userLogin();
       }
       return userLoginObject;
    }

   
    
    
    
    
