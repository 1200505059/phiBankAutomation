private void changePassword(){
    String newPassword=String.valueOf(this.txtNewPassword.getPassword());
      String newPasswordAgain=String.valueOf(this.txtNewPasswordAgain.getPassword());
      if (newPassword.equals(newPasswordAgain)) {
        this.passwordOkey();
    }else{
          Dialogs.showPrivateMessage(this, "Passwords do not match \n please try again!");
      }
}
private void passwordOkey(){
   this.getPasswChangeObject().setIdentityNumber(this.txtIdentity.getText());
      this.getPasswChangeObject().setPhoneNumber(this.txtPhoneNumber.getText());
         this.getPasswChangeObject().setSecurityAns(this.txtSecurtiyAns.getText());
        if (this.isEnabledOldPassword()) {
           this.getPasswChangeObject().setOldPassword(String.valueOf(this.txtOldPassword.getPassword()));
    }
   this.getPasswChangeObject().setNewPassword(String.valueOf(this.txtNewPassword.getPassword()));
    if (this.getPasswChangeObject().passwordIsChanged()) {
           Dialogs.showPrivateMessage(this, "Your password is change be successfully !");
           if (this.isEnabledOldPassword()) {
               ActionSettings.setVisible(this, new ChangePasswordScreen());
                          
        }else{
               ActionSettings.setVisible(this, new LoginScreen());
           }
    }else{
        Dialogs.showPrivateMessage(this, "Your password could not be renewed \n  please check your information.");
    }
}
    @Override
    public void getedits() {
        this.setLocationRelativeTo(null);
        passwordPagePanel.setFocusable(true);
        TextSettings.setOnlyNumber(txtIdentity);
        TextSettings.setMaxLimit(txtIdentity, 11);
        TextSettings.setOnlyNumber(txtPhoneNumber);
        TextSettings.setMaxLimit(txtPhoneNumber, 11);
        if (accountInformation().getUserID()==0) {
            this.txtOldPassword.setEnabled(false);
        }
    }
    public JPasswordField getOldPasswordText(){
        return txtOldPassword;
    }
    
    private boolean isEnabledOldPassword(){
        return this.getOldPasswordText().isEnabled();
    }

    @Override
    public boolean informationIsValid() {
return TextSettings.textAreaIsEmpty(this.passwordPagePanel);
    }

